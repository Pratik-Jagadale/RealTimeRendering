void resize(int width, int height)
{
	/* code */
	if (height == 0) // to avoid devided by zero
		height = 1;

	glViewport(0, 0, width, height);

	perspectiveProjectionMatrix = vmath::perspective(
		45.0f,
		(GLfloat)width / (GLfloat)height,
		0.1f,
		100.0f);
}

void display(void)
{
	// Function Prototype
	void pushMatrix(mat4);
	mat4 popMatrix(void);

	// variable declartions
	mat4 modelMatrix = mat4::identity();
	mat4 viewMatrix = mat4::identity();
	mat4 translationMatrix = mat4::identity();
	mat4 scaleMatrix = mat4::identity();

	mat4 rotationMatrix_Day;
	mat4 rotationMatrix_Year;

	vec3 eye = vec3(0.0f, 0.0f, 5.0f);
	vec3 center = vec3(0.0f, 0.0f, 0.0f);
	vec3 up = vec3(0.0f, 1.0f, 0.0f);

	/* Code */
	glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

	if (bPolygonModelIsLine == TRUE)
		glPolygonMode(GL_FRONT_AND_BACK, GL_LINE);
	else
		glPolygonMode(GL_FRONT_AND_BACK, GL_FILL);

	// use shader program obejct
	glUseProgram(shaderProgramObject);

	// View Transformation
	viewMatrix = vmath::lookat(eye, center, up); // like gluLookAt()

	// save the Camera Matrix "Push"
	// glPushMatrix()
	pushMatrix(viewMatrix);

	// No glRotatef(90.0f, 1.0f,0.0f,0.0f) call here as no gluSphere()

	// already handled glPolygonMode Above

	// Parallel to radius 0.75 given in gluSphere()
	scaleMatrix = vmath::scale(0.75f, 0.75f, 0.75f);

	modelMatrix = scaleMatrix;

	glUniformMatrix4fv(modelMatrixUniform, 1, GL_FALSE, modelMatrix);
	glUniformMatrix4fv(viewMatrixUniform, 1, GL_FALSE, viewMatrix);
	glUniformMatrix4fv(projectionMatrixUniform, 1, GL_FALSE, perspectiveProjectionMatrix);

	// gluSphere()
	glBindVertexArray(gVao_sphere);

	glVertexAttrib3f(PRJ_ATRIBUTE_COLOR, 1.0f, 1.0f, 0.0f);

	glBindBuffer(GL_ELEMENT_ARRAY_BUFFER, gVbo_sphere_element);

	glDrawElements(GL_TRIANGLES, gNumElements, GL_UNSIGNED_SHORT, 0);

	glBindBuffer(GL_ELEMENT_ARRAY_BUFFER, 0);

	glBindVertexArray(0);

	viewMatrix = popMatrix();

	// Using the restored "popped" camera Matrix
	pushMatrix(viewMatrix); // Push Two

	rotationMatrix_Year = vmath::rotate((GLfloat)year, 0.0f, 1.0f, 0.0f);
	translationMatrix = vmath::translate(1.5f, 0.0f, 0.0f);

	rotationMatrix_Day = vmath::rotate((GLfloat)day, 0.0f, 1.0f, 0.0f);

	scaleMatrix = vmath::scale(0.2f, 0.2f, 0.2f);

	modelMatrix = rotationMatrix_Year * translationMatrix; // Revolution Arround Sun
	modelMatrix = modelMatrix * rotationMatrix_Day;		   // Rotation of Earth Around itsewlf
	modelMatrix = modelMatrix * scaleMatrix;			   // Scale Earth to 0.2f;

	glUniformMatrix4fv(modelMatrixUniform, 1, GL_FALSE, modelMatrix);
	glUniformMatrix4fv(viewMatrixUniform, 1, GL_FALSE, viewMatrix);
	glUniformMatrix4fv(projectionMatrixUniform, 1, GL_FALSE, perspectiveProjectionMatrix);

	// gluSphere()
	glBindVertexArray(gVao_sphere);

	// glColor3f() for sun

	glVertexAttrib3f(PRJ_ATRIBUTE_COLOR, 0.4f, 0.9f, 1.0f);

	glBindBuffer(GL_ELEMENT_ARRAY_BUFFER, gVbo_sphere_element);

	glDrawElements(GL_TRIANGLES, gNumElements, GL_UNSIGNED_SHORT, 0);

	glBindBuffer(GL_ELEMENT_ARRAY_BUFFER, 0);

	glBindVertexArray(0);

	viewMatrix = popMatrix();

	// unuse the shader program object
	glUseProgram(0);

	SwapBuffers(ghdc);
}

void initializeMatrixStack(void)
{
	// code
	matrixStackTop = 0;
	for (int i = matrixStackTop; i < MODEL_VIEW_MATRIX_STACK; i++)
		matrixStack[i] = mat4::identity();
}

void pushMatrix(mat4 matrix)
{
	// Function Prototype
	void uninitialize(void);

	//	Code
	fprintf(gpFile, "Before Push Stack Top = %d\n", matrixStackTop);

	if (matrixStackTop > (MODEL_VIEW_MATRIX_STACK - 1))
	{
		fprintf(gpFile, "ERROR - EXCEEDED MATRIX STACK LIMIT:\n");
		uninitialize();
	}

	matrixStack[matrixStackTop] = matrix;
	matrixStackTop++;

	fprintf(gpFile, "After Push Stack Top = %d\n", matrixStackTop);
}

mat4 popMatrix(void)
{
	// function ptototype
	void uninitialize(void);

	// variable declartions
	mat4 matrix;

	// code
	fprintf(gpFile, "Before Pop Staqck Top = %d\n", matrixStackTop);

	if (matrixStackTop < 0)
	{
		fprintf(gpFile, "ERROR : MATRIX STACK EMPTY!\n");
		uninitialize();
	}

	matrixStack[matrixStackTop] = mat4::identity();
	matrixStackTop--;

	matrix = matrixStack[matrixStackTop];

	fprintf(gpFile, "After Pop Staqck Top = %d\n", matrixStackTop);

	return (matrix);
}

